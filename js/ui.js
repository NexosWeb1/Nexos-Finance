/* ---- Formatação monetária (valores em CENTAVOS) ---- */
export function fmtBRL(cents) {
  const v = (Number(cents) || 0) / 100;
  return v.toLocaleString("pt-BR", { style: "currency", currency: "BRL" });
}

export function fmtBRLshort(cents) {
  const v = (Number(cents) || 0) / 100;
  if (Math.abs(v) >= 1000) {
    return "R$ " + (v / 1000).toLocaleString("pt-BR", { maximumFractionDigits: 1 }) + "k";
  }
  return fmtBRL(cents);
}

/* "R$ 1.234,56" ou "1.234,56" -> centavos (inteiro) */
export function parseBRLtoCents(str) {
  if (typeof str === "number") return Math.round(str * 100);
  const digits = String(str).replace(/\D/g, "");
  return digits ? parseInt(digits, 10) : 0;
}

